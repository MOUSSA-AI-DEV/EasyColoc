<?php

namespace App\Models;

// use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;


class User extends Authenticatable
{
    /** @use HasFactory<\Database\Factories\UserFactory> */
    use HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var list<string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'reputation',
        'is_admin'
    ];

    protected static function boot()
    {
        parent::boot();

     
        static::created(function ($user) {
            if (User::count() === 1) {
                $user->update(['is_admin' => true]);
            }
        });
    }
    /**
     * The attributes that should be hidden for serialization.
     *
     * @var list<string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }
    public function expenses()
    {
        return $this->hasMany(Expense::class, 'payer_id');
    }

    public function colocations()
    {
        return $this->belongsToMany(Colocation::class)
            ->withPivot(['role', 'joined_at', 'left_at'])
            ->withTimestamps()
            ->as('membership');
    }

    public function activeColocation()
    {
        return $this->belongsToMany(Colocation::class)
            ->withPivot(['role', 'joined_at', 'left_at'])
            ->wherePivot('left_at', null)
            ->first();
    }

    public function invitations()
    {
        return $this->hasMany(Invitation::class, 'owner_id');
    }

    public function settlementsAsDebtor()
    {
        return $this->hasMany(Settlement::class, 'debtor_id');
    }

    public function settlementsAsCreditor()
    {
        return $this->hasMany(Settlement::class, 'creditor_id');
    }
}
